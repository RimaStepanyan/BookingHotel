package classwork.lesson4;

public class ifSample {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        if (x > y)
            System.out.println("x-ը մեծ է y-ից");
        x = x * 2;
        if (x == y)
            System.out.println("x-ը այժմ հավասար է y-ին");


    }
}
